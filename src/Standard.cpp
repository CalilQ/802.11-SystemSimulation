void Standard::set_standard(dot11_standard st, channel_bandwidth bw, bool sgi) {
	currentStd = st;
	band = bw;

	rollof = 0.1875;

	switch(st) {
	case dot11a : {
		maxMCS = MCS7;
		maxBand = MHz20;
		break;
	}
	case dot11n : {
		maxMCS = MCS7;
		maxBand = MHz40;
		break;
	}
	case dot11ac: {
		if(band == MHz20) maxMCS = MCS8;
		else maxMCS = MCS9;
		maxBand = MHz160;
		break;
	}
	case dot11ah: {
		maxMCS = MCS9;
		maxBand = MHz160;
		break;
	}
	default : {
		maxMCS = MCS;
		maxBand = MHz20;
	}
	}

	if(bw > maxBand)throw (my_exception("Bandwidth not supported by standard."));

	switch(bw){
	case MHz20:
		if(currentStd == dot11a)numSubcarriers = 52;
		else numSubcarriers = 56;
		lengthFFT = 64;
		break;
	case MHz40:
		numSubcarriers = 114;
		lengthFFT = 128;
		break;
	case MHz80:
		numSubcarriers = 242;
		lengthFFT = 256;
		break;
	case MHz160:
		numSubcarriers = 484;
		lengthFFT = 512;
		break;
	}

	if(st == dot11ah) symbol_period = 40e-6;
	else symbol_period = 4e-6;

	shortGI = sgi;
	if(shortGI) symbol_period = symbol_period - 0.4e-6;

	sgiIdx = 0;
	if(shortGI) sgiIdx = 1;
	bandIdx = band - MHz20;
}

